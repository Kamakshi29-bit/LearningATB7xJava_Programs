package ex_21072024;

public class Lab126 {
    public static void main(String[] args) {
        //loops - while and do while
        int i=0;
        while(i<=10){
            System.out.println(i);
            i++;
        }
        for(int j=0; j<=10;j++){
            System.out.println(j);
        }
    }
}
